#include "calc.h"

/**
 *op_add - adds
 *@a: integer 1
 *@b: integer 2
 *Return: sum
 */
int op_add(int a, int b)
{
return ((a) + (b));
}

/**
 *op_sub - subtracts
 *@a: integer 1
 *@b: integer 2
 *Return: sum
 */
int op_sub(int a, int b)
{
return ((a) - (b));
}

/**
 *op_mul - mulitiplies
 *@a: integer 1
 *@b: integer 2
 *Return: sum
 */
int op_mul(int a, int b)
{
return ((a) * (b));
}

/**
 *op_div - divides
 *@a: integer 1
 *@b: integer 2
 *Return: sum
 */
int op_div (int a, int b)
{
return ((a) / (b));
}

/**
 *op_mod - modulos
 *@a: integer 1
 *@b: integer 2
 *Return: sum
 */
int op_mod(int a, int b)
{
return ((a) % (b));
}
